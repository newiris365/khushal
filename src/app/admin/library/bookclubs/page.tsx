"use client";

import React, { useState, useEffect } from 'react';
import { Users, Plus, Calendar, BookOpen, Sparkles, MessageSquare, RefreshCw } from 'lucide-react';
import { apiGet, apiPost } from '../../../../lib/api';

export default function AdminBookClubsPage() {
  const [clubs, setClubs] = useState<any[]>([]);
  const [books, setBooks] = useState<any[]>([]);
  const [selectedClub, setSelectedClub] = useState<any>(null);
  const [discussions, setDiscussions] = useState<any[]>([]);
  const [loading, setLoading] = useState(true);
  const [loadingDiscussions, setLoadingDiscussions] = useState(false);

  // Create Club Form State
  const [newClubName, setNewClubName] = useState('');
  const [newClubBookId, setNewClubBookId] = useState('');
  const [newClubSchedule, setNewClubSchedule] = useState('');
  const [creatingClub, setCreatingClub] = useState(false);

  // Add Discussion Form State
  const [chapter, setChapter] = useState('');
  const [question, setQuestion] = useState('');
  const [aiGenerated, setAiGenerated] = useState(false);
  const [addingDiscussion, setAddingDiscussion] = useState(false);

  const [message, setMessage] = useState<{ type: 'success' | 'error'; text: string } | null>(null);

  useEffect(() => {
    loadClubsData();
  }, []);

  const loadClubsData = async () => {
    try {
      const [clubsRes, booksRes] = await Promise.all([
        apiGet('/library/book-clubs'),
        apiGet('/library/books')
      ]);

      if (clubsRes.success) {
        setClubs(clubsRes.book_clubs || []);
        if (clubsRes.book_clubs?.length > 0) {
          const current = selectedClub ? clubsRes.book_clubs.find((c: any) => c.id === selectedClub.id) : clubsRes.book_clubs[0];
          if (current) {
            setSelectedClub(current);
            loadDiscussions(current.id);
          }
        }
      }
      if (booksRes.success) {
        setBooks(booksRes.books || []);
      }
    } catch (err) {
      console.error(err);
      // fallback mocks
      setClubs([
        {
          id: 'c1',
          name: 'Algorithms & Computing club',
          schedule: 'Every Friday at 4 PM',
          members: ['s1', 's2', 's3'],
          current_book_id: 'b1',
          books: { title: 'Introduction to Algorithms', author: 'Thomas Cormen' }
        }
      ]);
      setBooks([
        { id: 'b1', title: 'Introduction to Algorithms', author: 'Thomas H. Cormen' },
        { id: 'b2', title: 'Design Patterns', author: 'Erich Gamma' }
      ]);
      setDiscussions([
        { id: 'd1', chapter: 'Chapter 1: Foundations', question: 'What are the main time complexities we care about in practice and why does asymptotic order matter?', ai_generated: false, responses: [] }
      ]);
    } finally {
      setLoading(false);
    }
  };

  const loadDiscussions = async (clubId: string) => {
    setLoadingDiscussions(true);
    try {
      const res = await apiGet(`/library/book-clubs/${clubId}/discussions`);
      if (res.success) {
        setDiscussions(res.discussions || []);
      }
    } catch {
      // ignore
    } finally {
      setLoadingDiscussions(false);
    }
  };

  const handleSelectClub = (club: any) => {
    setSelectedClub(club);
    loadDiscussions(club.id);
  };

  const handleCreateClub = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!newClubName.trim()) return;

    setCreatingClub(true);
    setMessage(null);
    try {
      const res = await apiPost('/library/book-clubs', {
        name: newClubName,
        current_book_id: newClubBookId || null,
        schedule: newClubSchedule || 'TBD'
      });

      if (res.success) {
        setMessage({ type: 'success', text: 'Book club created successfully!' });
        setNewClubName('');
        setNewClubBookId('');
        setNewClubSchedule('');
        loadClubsData();
      }
    } catch {
      setMessage({ type: 'error', text: 'Failed to create book club.' });
    } finally {
      setCreatingClub(false);
    }
  };

  const handleAddDiscussion = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!chapter.trim() || !question.trim() || !selectedClub) return;

    setAddingDiscussion(true);
    setMessage(null);
    try {
      const res = await apiPost(`/library/book-clubs/${selectedClub.id}/discussion`, {
        chapter,
        question,
        ai_generated: aiGenerated
      });

      if (res.success) {
        setMessage({ type: 'success', text: aiGenerated ? 'AI analytical question generated by Claude!' : 'Discussion thread added!' });
        setChapter('');
        setQuestion('');
        setAiGenerated(false);
        loadDiscussions(selectedClub.id);
      }
    } catch {
      setMessage({ type: 'error', text: 'Failed to add discussion thread.' });
    } finally {
      setAddingDiscussion(false);
    }
  };

  return (
    <main className="min-h-screen bg-[#0D0A1A] text-white pb-24">
      {/* Hero Header */}
      <div className="relative overflow-hidden">
        <div className="absolute inset-0 bg-gradient-to-br from-[#6C2BD9]/15 via-[#0D0A1A] to-[#0D0A1A]" />
        <div className="absolute top-0 right-0 w-96 h-96 bg-[#8B5CF6]/5 rounded-full blur-[120px]" />
        
        <div className="relative max-w-7xl mx-auto px-6 pt-10 pb-6 flex justify-between items-center">
          <div className="flex items-center gap-4">
            <div className="w-14 h-14 rounded-2xl bg-gradient-to-br from-[#6C2BD9] to-[#8B5CF6] flex items-center justify-center shadow-lg shadow-[#6C2BD9]/25">
              <Users className="w-7 h-7 text-white" />
            </div>
            <div>
              <h1 className="font-extrabold text-2xl lg:text-3xl text-white">Book Clubs Coordinator</h1>
              <p className="text-sm text-[#C4B5FD]/70">Admin Console • Schedule coordination, chapter discussions, and Claude AI prompts</p>
            </div>
          </div>
          <button onClick={loadClubsData} className="p-2.5 rounded-xl bg-white/5 border border-white/5 hover:bg-white/10 transition-all text-[#C4B5FD]/80 hover:text-white">
            <RefreshCw className="w-4 h-4" />
          </button>
        </div>
      </div>

      <div className="max-w-7xl mx-auto px-6 mt-6">
        {/* Status Messages */}
        {message && (
          <div className={`p-4 rounded-xl border mb-6 text-xs font-semibold ${
            message.type === 'success' ? 'bg-emerald-500/10 border-emerald-500/20 text-emerald-400' : 'bg-red-500/10 border-red-500/20 text-red-400'
          }`}>
            {message.text}
          </div>
        )}

        <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
          {/* Left Columns: Create Club Form + List */}
          <div className="lg:col-span-1 space-y-6">
            {/* Create Club */}
            <div className="glass-panel p-6 rounded-3xl border border-white/5 bg-[#13102A]/60 shadow-xl">
              <h3 className="text-sm font-bold text-white mb-4 flex items-center gap-2">
                <Plus className="w-4 h-4 text-[#A78BFA]" /> Register New Book Club
              </h3>
              <form onSubmit={handleCreateClub} className="space-y-4">
                <div>
                  <label className="block text-[10px] uppercase tracking-wider text-[#C4B5FD]/70 mb-1">Club Name</label>
                  <input
                    type="text"
                    placeholder="e.g. Data Science Reading Group"
                    value={newClubName}
                    onChange={(e) => setNewClubName(e.target.value)}
                    className="w-full bg-[#0D0A1A] border border-white/10 rounded-xl px-3 py-2.5 text-xs text-white focus:outline-none focus:border-[#6C2BD9]/50"
                    required
                  />
                </div>

                <div>
                  <label className="block text-[10px] uppercase tracking-wider text-[#C4B5FD]/70 mb-1">Assigned Book</label>
                  <select
                    value={newClubBookId}
                    onChange={(e) => setNewClubBookId(e.target.value)}
                    className="w-full bg-[#0D0A1A] border border-white/10 rounded-xl px-3 py-2.5 text-xs text-white focus:outline-none focus:border-[#6C2BD9]/50"
                  >
                    <option value="">-- Optional Book --</option>
                    {books.map(b => (
                      <option key={b.id} value={b.id}>{b.title} (by {b.author})</option>
                    ))}
                  </select>
                </div>

                <div>
                  <label className="block text-[10px] uppercase tracking-wider text-[#C4B5FD]/70 mb-1">Meeting Schedule</label>
                  <input
                    type="text"
                    placeholder="e.g. Every Friday at 4 PM"
                    value={newClubSchedule}
                    onChange={(e) => setNewClubSchedule(e.target.value)}
                    className="w-full bg-[#0D0A1A] border border-white/10 rounded-xl px-3 py-2.5 text-xs text-white focus:outline-none focus:border-[#6C2BD9]/50"
                  />
                </div>

                <button
                  type="submit"
                  disabled={creatingClub || !newClubName.trim()}
                  className="w-full py-2.5 rounded-xl bg-[#6C2BD9] hover:bg-[#8B5CF6] text-xs font-bold text-white transition-all shadow-lg"
                >
                  {creatingClub ? 'Registering...' : 'Register Club'}
                </button>
              </form>
            </div>

            {/* List of Clubs */}
            <div className="space-y-3">
              <h3 className="text-xs font-bold text-[#C4B5FD]/70 uppercase tracking-wider">All Active Clubs</h3>
              {loading ? (
                <div className="flex justify-center py-6">
                  <div className="w-6 h-6 border-2 border-[#6C2BD9] border-t-transparent rounded-full animate-spin" />
                </div>
              ) : clubs.map((club) => {
                const isActive = selectedClub?.id === club.id;
                return (
                  <button
                    key={club.id}
                    onClick={() => handleSelectClub(club)}
                    className={`w-full text-left p-4 rounded-2xl border transition-all flex justify-between items-center ${
                      isActive
                        ? 'bg-[#6C2BD9]/20 border-[#6C2BD9]/40 text-[#A78BFA]'
                        : 'bg-[#13102A]/60 border-white/5 hover:bg-[#1A1538]'
                    }`}
                  >
                    <div>
                      <h4 className="text-xs font-bold text-white">{club.name}</h4>
                      <p className="text-[9px] text-[#C4B5FD]/50 mt-1">{club.schedule}</p>
                    </div>
                    <span className="text-[10px] bg-white/5 px-2 py-1 rounded-lg text-[#C4B5FD]">{club.members?.length || 0} Members</span>
                  </button>
                );
              })}
            </div>
          </div>

          {/* Right Column: Manage Selected Club Chapters & Discussions */}
          <div className="lg:col-span-2 space-y-6">
            {selectedClub ? (
              <div className="glass-panel p-6 rounded-3xl border border-white/5 bg-[#13102A]/60 shadow-xl space-y-6">
                <div>
                  <span className="text-[9px] font-mono text-[#C4B5FD]/40">Club Coordinator Console</span>
                  <h2 className="text-xl font-extrabold text-white mt-1">{selectedClub.name}</h2>
                  <p className="text-xs text-[#C4B5FD]/70 mt-1">Book: <span className="text-white font-bold">{selectedClub.books?.title || 'None'}</span></p>
                </div>

                <hr className="border-white/5" />

                {/* Add Chapter Discussion Form */}
                <div className="p-5 rounded-2xl bg-white/5 border border-white/5 space-y-4">
                  <h4 className="text-xs font-bold text-white uppercase tracking-wider">Launch Chapter Discussion Thread</h4>
                  <form onSubmit={handleAddDiscussion} className="space-y-4">
                    <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
                      <div className="md:col-span-1">
                        <label className="block text-[9px] uppercase tracking-wider text-[#C4B5FD]/60 mb-1">Chapter Name</label>
                        <input
                          type="text"
                          placeholder="e.g. Chapter 1"
                          value={chapter}
                          onChange={(e) => setChapter(e.target.value)}
                          className="w-full bg-[#0D0A1A] border border-white/10 rounded-xl px-3 py-2.5 text-xs text-white focus:outline-none focus:border-[#6C2BD9]/50"
                          required
                        />
                      </div>
                      <div className="md:col-span-2">
                        <label className="block text-[9px] uppercase tracking-wider text-[#C4B5FD]/60 mb-1">Topic / Custom Prompt</label>
                        <input
                          type="text"
                          placeholder={aiGenerated ? "e.g. Algorithmic design concepts" : "e.g. Discuss standard growth rates..."}
                          value={question}
                          onChange={(e) => setQuestion(e.target.value)}
                          className="w-full bg-[#0D0A1A] border border-white/10 rounded-xl px-3 py-2.5 text-xs text-white focus:outline-none focus:border-[#6C2BD9]/50"
                          required
                        />
                      </div>
                    </div>

                    <div className="flex items-center justify-between">
                      <div className="flex items-center gap-2">
                        <input
                          type="checkbox"
                          id="ai-generate"
                          checked={aiGenerated}
                          onChange={(e) => setAiGenerated(e.target.checked)}
                          className="accent-[#6C2BD9]"
                        />
                        <label htmlFor="ai-generate" className="text-xs text-[#C4B5FD]/85 cursor-pointer flex items-center gap-1.5">
                          <Sparkles className="w-3.5 h-3.5 text-[#A78BFA] animate-pulse" /> Ask Claude to auto-generate high-quality analytical question
                        </label>
                      </div>

                      <button
                        type="submit"
                        disabled={addingDiscussion || !chapter.trim() || !question.trim()}
                        className="px-5 py-2.5 rounded-xl bg-[#6C2BD9] hover:bg-[#8B5CF6] text-xs font-bold text-white transition-all shadow-md disabled:opacity-55"
                      >
                        {addingDiscussion ? 'Launching...' : 'Launch Discussion'}
                      </button>
                    </div>
                  </form>
                </div>

                {/* List of active chapter discussions */}
                <div className="space-y-4">
                  <h4 className="text-xs font-bold text-[#C4B5FD]/70 uppercase tracking-wider">Active Chapter Discussion Threads</h4>
                  
                  {loadingDiscussions ? (
                    <div className="flex justify-center py-6">
                      <div className="w-6 h-6 border-2 border-[#6C2BD9] border-t-transparent rounded-full animate-spin" />
                    </div>
                  ) : discussions.map((d) => (
                    <div key={d.id} className="p-4 rounded-xl bg-[#0D0A1A]/50 border border-white/5 flex justify-between items-start gap-4">
                      <div className="space-y-1">
                        <div className="flex items-center gap-2">
                          <span className="text-[9px] font-bold uppercase tracking-wider text-[#A78BFA] bg-[#6C2BD9]/20 px-2 py-0.5 rounded">{d.chapter}</span>
                          {d.ai_generated && <span className="text-[8px] bg-purple-500/10 border border-purple-500/20 text-purple-400 font-bold px-1.5 py-0.5 rounded flex items-center gap-0.5"><Sparkles className="w-2.5 h-2.5" /> AI</span>}
                        </div>
                        <p className="text-xs text-white leading-relaxed font-semibold">"{d.question}"</p>
                      </div>
                      <span className="text-[10px] text-[#C4B5FD]/50 whitespace-nowrap">{d.responses?.length || 0} Replies</span>
                    </div>
                  ))}

                  {discussions.length === 0 && (
                    <p className="text-[10px] text-[#C4B5FD]/30 py-6 text-center">No discussion topics launched yet.</p>
                  )}
                </div>
              </div>
            ) : (
              <div className="glass-panel p-12 rounded-3xl border border-white/5 bg-[#13102A]/60 shadow-xl text-center text-xs text-[#C4B5FD]/30">
                Select a book club from the left list to manage discussion threads.
              </div>
            )}
          </div>
        </div>
      </div>
    </main>
  );
}
